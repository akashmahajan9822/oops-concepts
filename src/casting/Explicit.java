package casting;

public class Explicit {
	
	
	public static void main(String[] args) {
		
	    //Data type 
		
			
			//Higher size-
			double d =125.55;
			
			//Lower size
			int i=(int)d;
			
			
			
			//double d----->int i
			//This is called ass explicit type of casting
			
			System.out.println(i);
			
			
		}

}
